# Usados para comparar valores. O resultado é sempre True ou False.
print(5 == 5)   # Igual a (==): True
print(5 != 3)   # Diferente de (!=): True
print(5 > 3)    # Maior que: True
print(5 < 3)    # Menor que: False
print(5 >= 5)   # Maior ou igual: True
print(3 <= 5)   # Menor ou igual: True