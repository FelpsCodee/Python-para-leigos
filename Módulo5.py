a = 10
b = 3
print(a + b)   # Soma (+): 10 + 3 = 13
print(a - b)   # Subtração (-): 10 - 3 = 7
print(a * b)   # Multiplicação (*): 10 * 3 = 30
print(a / b)   # Divisão (/): 10 / 3 = 3.333...
print(a // b)  # Divisão inteira (//): 10 // 3 = 3 (descarta as casas decimais)
print(a % b)   # Resto da divisão (%): 10 % 3 = 1
print(a ** b)  # Potência (**): 10 ** 3 = 1000