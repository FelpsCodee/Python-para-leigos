# Variáveis são nomes que guardam valores. Você pode mudar o valor delas quando quiser.
nome = "João"         # String (texto, sempre entre aspas)
idade = 30            # Inteiro (número sem vírgula)
altura = 1.75         # Float (número com ponto)
ativo = True          # Booleano (True ou False, sempre com a primeira letra maiúscula)

# Para ver o tipo de uma variável, use type():
print(type(nome))     # <class 'str'>
print(type(idade))    # <class 'int'>
print(type(altura))   # <class 'float'>
print(type(ativo))    # <class 'bool'>
